
//Translate from English to Morse Code (destructuring assignment)
const engToMorse = {
    a:".-", b:"-...", c:"-.-.", d:"-..", e:".",
    f:"..-.", g:"--.", h:"....", i:"..", j:".---",
    k:"-.-", l:".-..", m:"--", n:"-.", o:"---",
    p:".--.", q:"--.-", r:".-.", s:"...", t:"-", 
    u:"..-", v:"...-", w:".--", x:"-..-", y:"-.--", 
    z:"--..", 
    1:".----", 2:"..---", 3:"...--", 4:"....-",
    5:".....", 6:"-....", 7:"--...", 8:"---..", 9:"----.",
    0:"-----",
    "?":"..--..", "!":".-....", ",":"--..--", ".":".-.-.-", ":":".----.",
    " ": " / ",
    
}

const morseToEng = {
    ".-":"a", "-...":"b", "-.-.":"c", "-..":"d", ".":"e",
    "..-.":"f", "--.":"g", "....":"h", "..":"i", ".---":"j",
    "-.-":"k", ".-..":"l", "--":"m", "-.":"n", "---":"o",
    ".--.":"p", "--.-":"q", ".-.":"r", "...":"s", "-":"t", 
    "..-":"u", "...-":"v", ".--":"w", "-..-":"x", "-.--":"y", 
    "--..":"z", 
    ".----":1, "..---":2, "...--":3, "....-":4,
    ".....":5, "-....":6, "--...":7, "---..":8, "----.":9,
    "-----":0,
    "..--..": "?", ".-....": "!", "--..--":",", ".-.-.-":".", ".----.":":",
    "/": " ",
}

export const fromEngToMorse = (string) => {
    return string.toLowerCase().split("")
    .map((string) => {
      return engToMorse[string];
      })
    .join(" ");
  };

  export const fromMorseToEng = (string) => {
    return string.split(" ")
    .map((string) => {
      return morseToEng[string];
      })
    .join("");
  };









